// ========================================================
// 학습된 가중치 로드 (시뮬레이션)
// 실제 환경에서는 수천 개의 숫자를 C 배열로 변환해서 가져옵니다.
// ========================================================
static inline void load_trained_weights(float *conv_kernel, float *dense_weight,
                                        float *dense_bias) {
    // 1. [수정] 음수를 발생시키지 않고 형태를 유지/강화하는 블러(Blur) 커널
    float blur_kernel[9] = {0.1f, 0.1f, 0.1f, 0.1f, 0.5f, 0.1f, 0.1f, 0.1f, 0.1f};
    for (int i = 0; i < 9; i++)
        conv_kernel[i] = blur_kernel[i];

    // 2. 가중치 초기화 (나머지 숫자들의 확률은 낮춤)
    for (int i = 0; i < 14 * 14 * 10; i++) {
        dense_weight[i] = -0.1f;
    }
    for (int i = 0; i < 10; i++) {
        dense_bias[i] = 0.0f;
    }

    // 3. [수정] 실제 풀링된 숫자 7의 위치(14x14)에 맞춰 타겟팅!
    for (int y = 0; y < 14; y++) {
        for (int x = 0; x < 14; x++) {
            int pool_idx = y * 14 + x;

            // '7'의 상단 가로선 특징 위치 (y: 3~4, x: 4~10)
            if (y >= 3 && y <= 4 && x >= 4 && x <= 10) {
                dense_weight[pool_idx * 10 + 7] = 1.0f;
            }
            // '7'의 대각선 특징 위치 (y: 5~11, x: 4~9 언저리)
            else if (y >= 5 && y <= 11 && x >= 4 && x <= 9) {
                dense_weight[pool_idx * 10 + 7] = 0.8f;
            }
        }
    }
}

